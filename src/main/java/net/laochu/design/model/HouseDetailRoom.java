package net.laochu.design.model;

public class HouseDetailRoom {
	
	private Integer room_id;
	private Double area;
	private String shape_name;
	private Integer meter_power;
	private Double room_power;
	public Integer getRoom_id() {
		return room_id;
	}
	public void setRoom_id(Integer room_id) {
		this.room_id = room_id;
	}
	public Double getArea() {
		return area;
	}
	public void setArea(Double area) {
		this.area = area;
	}
	public String getShape_name() {
		return shape_name;
	}
	public void setShape_name(String shape_name) {
		this.shape_name = shape_name;
	}
	public Integer getMeter_power() {
		return meter_power;
	}
	public void setMeter_power(Integer meter_power) {
		this.meter_power = meter_power;
	}
	public Double getRoom_power() {
		return room_power;
	}
	public void setRoom_power(Double room_power) {
		this.room_power = room_power;
	}
	
	
	
}
